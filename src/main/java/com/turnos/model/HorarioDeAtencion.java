package com.turnos.model;

public class HorarioDeAtencion {
	
	private String diaSemana;
	private String horaDesde;
	private String horaHasta;
	private String legajoTerapista;
//	private Terapista terapista;
	
	
	public String getDiaSemana() {
		return diaSemana;
	}
	public void setDiaSemana(String diaSemana) {
		this.diaSemana = diaSemana;
	}
	public String getHoraDesde() {
		return horaDesde;
	}
	public void setHoraDesde(String horaDesde) {
		this.horaDesde = horaDesde;
	}
	public String getHoraHasta() {
		return horaHasta;
	}
	public void setHoraHasta(String horaHasta) {
		this.horaHasta = horaHasta;
	}
	public String getLegajoTerapista() {
		return legajoTerapista;
	}
	public void setLegajoTerapista(String legajoTerapista) {
		this.legajoTerapista = legajoTerapista;
	}
	
	

}
